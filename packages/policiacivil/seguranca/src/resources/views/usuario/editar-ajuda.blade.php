<p>
    Nesta tela você pode alterar informações de um usuário já cadastrado.<br>
</p>
<p>
    <b>Nome</b> e <b>E-mail</b> são campos obrigatórios.<br>
</p>
<p>
    <b>Senha</b> e <b>Confirmar senha</b> devem ser preenchidos somente se a intenção for de alterar a senha do usuário.<br>
</p>
<p>
    <b>CPF</b> e <b>Data de nascimento</b> são campos usados apenas quando o usuário desejar receber a senha
    do seu login via email na tela de <a target="_blank" href="{{url('seguranca/usuario/passo1')}}">"Preciso de ajuda"</a> na tela de login<br>
</p>
<p>
    <b>Perfis cadastrados no sistema</b><br>
    Este campo é que define quais serão os perfis que o usuário terá dentro do sistema. Cada perfil tem suas
    permissões e pode realizar determinadas tarefas dentro do sistema. Um usuário pode ter mais de um perfil
    se sua chefia ou os desenvolvedores acharem necessário, assim suas permissões são acumulativas.<br>
    O Perfil de administrador do sistema é o único com acesso total a todos os recursos do sistema. Suas
    permissões são dadas via código fonte e não por cadastro em banco de dados. Este perfil é recomendado apenas
    aos desenvolvedores.
</p>
